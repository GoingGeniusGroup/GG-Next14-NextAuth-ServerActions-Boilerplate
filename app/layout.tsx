import { Toaster } from "@/ui/sonner";
import type { Metadata } from "next";
import Providers from "@/app/providers/SessionProvider";
import { Inter } from "next/font/google";
import "@/styles/globals.css";
import { AuroraBackground } from "@/components/ui/background/aurora-background";
import { ThemeSwitcher } from "@/components/comp/ThemeToggler/ThemeSwitcher";
import { ThemeProvider } from "next-themes";
import FloatingDockInvertedComponent from "@/components/comp/dock/FloatingDockInverted";
import { UserProvider } from "@/hooks/UserProvider";
import { ToastProvider } from "./providers/toast-provider";
import { MobileSimulatorProvider } from "@/components/comp/MobileSimulator/provider/MobileSimulatorContext";
import { currentUser } from "@/lib/auth";
import { getUserAvatars } from "@/actions/avatar";
import {
  AvatarProvider,
  AvatarType,
} from "@/components/comp/AvatarManager/provider/AvatarManagerContext";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: {
    default: "Next Dashboard ",
    template: "Next Dashboard",
  },
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const user = await currentUser();

  if (!user) {
    return (
      <>
        <html lang="en" className="h-full">
          <body className={inter.className}>
            <Toaster position="bottom-left" richColors theme="light" />
            <Providers>
              <MobileSimulatorProvider>
                <ThemeProvider
                  attribute="class"
                  defaultTheme="system"
                  enableSystem
                >
                  <AuroraBackground>
                    <UserProvider>
                      {/* Theme switcher */}
                      <div className="absolute top-[8px] right-[73px] z-50">
                        <ThemeSwitcher />
                      </div>

                      {/* Dock section */}
                      <div className="w-full z-40">
                        <div className="p-4 text-black dark:text-white">
                          <FloatingDockInvertedComponent />
                        </div>
                      </div>

                      {/* Content section */}
                      <div className="flex-1 px-8 py-4 w-full overflow-auto">
                        {children}
                      </div>
                    </UserProvider>
                    <ToastProvider />
                  </AuroraBackground>
                </ThemeProvider>
              </MobileSimulatorProvider>
            </Providers>
          </body>
        </html>
      </>
    );
  }

  const avatarsResponse = await getUserAvatars(user.gg_id);
  const avatars: AvatarType[] =
    avatarsResponse.success && Array.isArray(avatarsResponse.data)
      ? avatarsResponse.data
      : [];

  return (
    <html lang="en" className="h-full">
      <body className={inter.className}>
        <Toaster position="bottom-left" richColors theme="light" />
        <Providers>
          <MobileSimulatorProvider>
            <ThemeProvider attribute="class" defaultTheme="system" enableSystem>
              <AuroraBackground>
                <UserProvider>
                  <AvatarProvider initialAvatars={avatars} user={user}>
                    {/* Theme switcher */}
                    <div className="absolute top-[8px] right-[73px] z-50">
                      <ThemeSwitcher />
                    </div>

                    {/* Dock section */}
                    <div className="w-full z-40">
                      <div className="p-4 text-black dark:text-white">
                        <FloatingDockInvertedComponent />
                      </div>
                    </div>

                    {/* Content section */}
                    <div className="flex-1 px-8 py-4 w-full overflow-auto">
                      {children}
                    </div>
                  </AvatarProvider>
                </UserProvider>
                <ToastProvider />
              </AuroraBackground>
            </ThemeProvider>
          </MobileSimulatorProvider>
        </Providers>
      </body>
    </html>
  );
}
